public class Switch{

   public static void main(String args[]){
     int num=2;
     switch(num+2)
     {
        case 1:
	  System.out.println("Value is: "+num);
	case 2:
	  System.out.println("Value is: "+num);
	case 3:
	  System.out.println("Value is: "+num);
        default:
	  System.out.println("Value is: "+num);
      }
   }
}